# 모든 단어의 첫 문자는 대문자.
# 그 외의 알파벳은 소문자인 문자열로 바꾼다.

# 단, 첫 문자가 알파벳이 아닐 때에는
# 이어지는 알파벳은 소문자로 바꾼다.

# s는 알파벳, 숫자, 공백문자(" ")로 이루어져 있다.
# 숫자는 단어의 첫 문자로만 나온다.
# 숫자로만 이루어진 단어는 없다.
# 공백 문자가 연속해서 나올 수 있다.

def solution(s):
    # 최종 결과 문자열
    answer = ''

    # 공백 하나(" ")를 기준으로 문자열을 나눈다.
    # split(" ")을 쓰는 이유:
    # 공백이 연속해서 나올 때 빈 문자열("")도 보존하기 위해서.
    # 예: "hello  world".split(" ")
    # 결과: ["hello", "", "world"]
    words = s.split(" ")

    # words의 인덱스를 하나씩 확인한다.
    # i = 현재 단어의 위치
    for i in range(len(words)):
        # 현재 처리할 단어
        word = words[i]

        # word가 빈 문자열이면 처리할 글자가 없다.
        # 빈 문자열에서 word[0]을 하면 에러가 난다.
        # 그래서 아무 문자도 붙이지 않는다.
        if word == "":
            answer += ""

        # word가 빈 문자열이 아니면 JadenCase로 바꾼다.
        else:
            # word[0].upper()
            # 첫 글자를 대문자로 바꾼다.
            # 첫 글자가 숫자면 upper()를 해도 그대로 숫자다.
            #
            # word[1:].lower()
            # 두 번째 글자부터 끝까지 소문자로 바꾼다.
            answer += word[0].upper() + word[1:].lower()

        # 마지막 단어가 아니면 뒤에 공백을 하나 붙인다.
        # split(" ")으로 나눴기 때문에
        # 다시 단어 사이에 공백을 넣어야 원래 공백 개수가 유지된다.
        if i != len(words) - 1:
            answer += " "

    # 완성된 문자열 반환
    return answer