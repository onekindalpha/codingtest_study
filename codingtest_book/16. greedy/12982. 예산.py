# 예산 문제

# 문제 요약
# 부서별로 물품 구매에 필요한 신청 금액이 주어진다.
# 전체 예산 budget이 정해져 있어 모든 부서를 지원할 수는 없다.
# 각 부서는 신청한 금액을 전부 지원받아야 하며, 일부 금액만 지원할 수는 없다.
# 예산 안에서 최대한 많은 부서를 지원해야 한다.

# 입력
# d: 부서별 신청 금액이 들어 있는 배열
# budget: 전체 예산

# 출력
# 예산 안에서 지원할 수 있는 최대 부서 수

# 그리디 판단
# 부서 하나를 지원하면 결과는 항상 +1개이다.
# 따라서 같은 1개를 지원할 거라면, 신청 금액이 작은 부서부터 지원하는 것이 유리하다.
# 그래서 d를 오름차순 정렬한 뒤, 예산이 허용하는 동안 작은 금액부터 차례대로 지원한다.

def solution(d, budget):
    d.sort()
    count = 0

    for amount in d:
        if budget >= amount:
            budget -= amount
            count += 1
        else:
            break
    return count

print(solution([1, 3, 2, 5, 4], 9))  # 3
print(solution([2, 2, 3, 3], 10))    # 4